// Variaveis

//let é uma palavra reservada para criar variáveis.
//ou seja, um espaço na memória do computador para armazenar um valor

let animal = "Cachorro"; // A variável "animal" é declarada usando a palavra-chave "let" e é atribuída a string "Cachorro".
console.log(animal); // O valor da variável "animal" é impresso no console, resultando em "Cachorro".

animal = "Gato";
console.log(animal);

// const é uma palavra reservada para criar constantes, ou seja, um espaço na memória do computador para armazenar um valor que não pode ser alterado.
const PI = 3.14; // A constante "PI" é declarada usando a palavra-chave "const" e é atribuída ao valor 3.14159.
console.log(PI); // O valor da constante "PI" é impresso no console, resultando em 3.14.

PI = 3.14159;
